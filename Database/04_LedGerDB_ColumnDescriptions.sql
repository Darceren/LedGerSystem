/*
================================================================================
  LedGerDB - Column Descriptions (MS_Description)
  Execute on existing database after 01/02 scripts.
  Safe to re-run: updates descriptions if they already exist.

  View in SSMS (important):
    1) Right-click table -> Design
    2) Click ONE column in the grid (e.g. Amount)
    3) Open bottom pane: "Column Properties"
    4) Find property "Description" (NOT the grid header comment)
    Or query:
      SELECT c.name, ep.value
      FROM sys.columns c
      LEFT JOIN sys.extended_properties ep
        ON ep.major_id=c.object_id AND ep.minor_id=c.column_id AND ep.name='MS_Description'
      WHERE c.object_id=OBJECT_ID('dbo.LedgerTransaction');

  Execute tip:
    - Recommended: open this file in SSMS and press F5
    - If using sqlcmd, file MUST be UTF-8 with BOM
================================================================================
*/

