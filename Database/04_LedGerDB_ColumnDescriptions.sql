CREATE OR ALTER PROCEDURE dbo.usp_SetDescription
    @TableName  SYSNAME,
    @ColumnName SYSNAME = NULL,
    @Description NVARCHAR(3750)
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @level2type VARCHAR(128) = NULL;
    DECLARE @level2name SYSNAME = NULL;

    IF @ColumnName IS NOT NULL
    BEGIN
        SET @level2type = 'COLUMN';
        SET @level2name = @ColumnName;
    END

    IF EXISTS (
        SELECT 1
        FROM sys.extended_properties ep
        INNER JOIN sys.tables t ON ep.major_id = t.object_id
        WHERE t.object_id = OBJECT_ID(N'dbo.' + @TableName)
          AND ep.name = N'MS_Description'
          AND ep.class = 1
          AND (
                (@ColumnName IS NULL AND ep.minor_id = 0)
             OR (@ColumnName IS NOT NULL AND ep.minor_id = COLUMNPROPERTY(OBJECT_ID(N'dbo.' + @TableName), @ColumnName, 'ColumnId'))
          )
    )
        EXEC sys.sp_updateextendedproperty
            @name = N'MS_Description', @value = @Description,
            @level0type = N'SCHEMA', @level0name = N'dbo',
            @level1type = N'TABLE',  @level1name = @TableName,
            @level2type = @level2type, @level2name = @level2name;
    ELSE
        EXEC sys.sp_addextendedproperty
            @name = N'MS_Description', @value = @Description,
            @level0type = N'SCHEMA', @level0name = N'dbo',
            @level1type = N'TABLE',  @level1name = @TableName,
            @level2type = @level2type, @level2name = @level2name;
END
GO

